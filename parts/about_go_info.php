<h3 class="mt-5 mb-3">About Info</h3>
<p>
    Go is a procedural programming language. It was developed in 2007 by Robert Griesemer, Rob Pike, 
    and Ken Thompson at Google but launched in 2009 as an open-source programming language. Programs are assembled 
    by using packages, for efficient management of dependencies. This language also supports environment adopting patterns 
    alike to dynamic languages. 
</p>
					